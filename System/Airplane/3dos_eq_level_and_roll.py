g = 9.807 #m/s2


n = 801 #要素数
t_list =[i*80/(n-1) for i in range(0, n)]
phi_list = [0 for i in t_list[:math.ceil((n-1)/80*20)+1]] + [np.pi/6*np.sin(np.pi/20*(i-20))
 for i in t_list[math.ceil((n-1)/80*20)+1:math.ceil((n-1)/80*60)+1]] + [0 for i in t_list[math.ceil((n-1)/80*60)+1:]]

A = rho/2*pow(U,2)*S
def U_eq_3dos(alpha, T):
    return T*np.cos(alpha) - A*(CD0+K*pow(alpha,2))
def gamma_eq_3dos(alpha, T, phi):
    return (A*CLa*alpha + T*np.sin(alpha)) * np.cos(phi) -m*g 

def result_calc():
    alpha_list = np.linspace(0, 0.3, 3001) #rad
    T_list = np.linspace(20000, 30000, 101) #N

    thread = 50
    for index,phi in enumerate(phi_list):
        sec = index*80/(n-1)
        if sec > 20 and sec < 60:
            for i in alpha_list:
                for j in T_list:
                    U_result = U_eq_3dos(i, j)
                    gamma_result = gamma_eq_3dos(i, j, phi)
                    if abs(U_result)<thread and abs(gamma_result)<thread:
                        print("[{}, {}, {}, {}],".format(i, j, sec, phi))

